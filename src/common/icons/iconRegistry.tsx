/**
 * Resolve an icon token (emoji char OR Lucide name) to the Lucide component.
 * Returns `null` when the token doesn't match anything we know about.
 */
export function resolveIcon(token: string | undefined | null): LucideIcon | null {
  if (!token) return null;
  // Try direct Lucide name first (new code path)
  const direct = NAME_TO_ICON[token];
  if (direct) return direct;
  // Fall back to emoji → name → component (legacy data path)
  const name = EMOJI_TO_NAME[token];
  if (name) return NAME_TO_ICON[name] ?? null;
  return null;
}

interface IconProps {
  /** Either an emoji ("📊") or a Lucide name ("FileText"). */
  token: string | undefined | null;
  /** Pixel size. Defaults to 16 for inline UI. */
  size?: number;
  /** Stroke width. Lucide default is 2; use 1.5 for finer chrome. */
  strokeWidth?: number;
  /** Fallback ReactNode if token doesn't resolve. Defaults to null. */
  fallback?: ReactNode;
  /** Optional className passthrough. */
  className?: string;
  /** Optional style passthrough. */
  style?: React.CSSProperties;
  /** ARIA label; if omitted icon is treated decorative (aria-hidden). */
  ariaLabel?: string;
}

/**
 * Universal icon component. Accepts both emoji strings (legacy) and Lucide
 * names (preferred for new code). Renders `currentColor` so it inherits the
 * parent text colour and works with the Skin Slot contract.
 */
export function Icon({
  token,
  size = 16,
  strokeWidth = 2,
  fallback = null,
  className,
  style,
  ariaLabel,
}: IconProps): ReactNode {
  const Resolved = resolveIcon(token);
  if (!Resolved) return fallback;
  return (
    <Resolved
      size={size}
      strokeWidth={strokeWidth}
      className={className}
      style={style}
      aria-label={ariaLabel}
      aria-hidden={ariaLabel ? undefined : true}
    />
  );
}

