/**
 * Icon registry — single source of truth for all UI icons.
 *
 * Why this exists: Windows + Chrome renders Unicode emoji (📊 ⚡ 🎯 …) via
 * Segoe UI Emoji as colourful 3D glyphs that clash with our cool, line-based
 * design language and break the Skin Slot contract (cannot inherit
 * currentColor). This registry maps every emoji that appears in legacy data
 * (node definitions, connector configs, wizard steps, etc.) onto the
 * matching Lucide line icon, and exposes a single <Icon token=... /> that
 * accepts either an emoji or a Lucide name.
 *
 * Usage:
 *   <Icon token={node.icon} size={16} />          // legacy emoji data still works
 *   <Icon token="FileText" size={16} />            // new code uses Lucide names
 *   <Icon token={undefined} fallback={<Plus />} /> // explicit fallback
 *
 * Adding new mappings: extend EMOJI_TO_NAME below. Lucide icons used in JSX
 * elsewhere should also be re-exported via the named import from this file
 * so we keep the icon set discoverable.
 */
